var num1 = 20;
var num2 = 5;
var num3 = 7;

// + additional operator
var additionResult = num1 + num2 + num3;
console.log("My Addition Result is", additionResult);

// - substraction operator
var subResult = num1 - num2 - num3;
console.log("My Substraction Result is", subResult);

// * Multiplication operator
var mulResult = num1 * num2 * num3;
console.log("My Multiplication Result is", mulResult);

// / division operator
var divisionResult = num1 / num2 / num3;
console.log("My Division Result is", divisionResult);

// % modules operator
var modulesResult = num1 % num2 % num3;
console.log("My Modules Result is", modulesResult);


// ** power operator
var powerResult = num1 ** num2;
console.log("My Power Result is", powerResult);

// 2^3 => 2 * 2 * 2 => 8 
// 4^2 => 4 * 4 => 16